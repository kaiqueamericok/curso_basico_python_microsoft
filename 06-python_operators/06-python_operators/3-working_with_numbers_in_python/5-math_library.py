# importanto funcões do modulo math, esse modulo contém diversas funções matemáticas úteis, incluindo aquelas para arredondamento

from math import ceil, floor

round_up = ceil(12.5) 
print(round_up)

# nesse exemplo o número é arrendondado para baixo, usando a função "floor" 
round_down = floor(12.5)
print(round_down)
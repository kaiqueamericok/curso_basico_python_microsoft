# elif no pyhton é a abreviação de else if 

a = 27
b = 93
if a <= b:	# Verifica se  "a" é menor ou igual a "b" se for verdadeira o proximo bloco de código é executado 
    print("a is less than or equal to b") # se "a" for menor ou igual a "b", essa linha será executada
elif a == b:   		# Se a condição  "a" é menor ou igual a "b" não for verdadeira, mas a == b for verdadeira, ele irá executar a linha de baixo 
    print("a is equal to b") # se a == b for verdadeiro essa linha é executado. 

# Sendo assim, o elif print(" a is equal to b") não será executado, pois a instrulção de if "a <=b" é true.



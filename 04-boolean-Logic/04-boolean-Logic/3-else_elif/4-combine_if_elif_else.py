a = 27
b = 93
if a < b:        # se a 27 é menor que 93 executa a linha abaixo
    print("a is less than b") 
elif a > b:      # se 27 < 93 for false, e se 27 é maior que 93, a linha abaixo será executada
    print("a is greater than b")
else: 	 	#se nenhuma das condições acima forem verdadeira, a linha abaixo será executada
    print ("a is equal to b")    






